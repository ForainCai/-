package com.zking.pojo;

import java.util.Date;

public class UserInfo {
    private Integer userinfoId;

    private Date userinfoTime;

    private String userinfoCard;

    private String userinfoMail;

    private String userinfoPhone;

    private String userinfoSex;

    public Integer getUserinfoId() {
        return userinfoId;
    }

    public void setUserinfoId(Integer userinfoId) {
        this.userinfoId = userinfoId;
    }

    public Date getUserinfoTime() {
        return userinfoTime;
    }

    public void setUserinfoTime(Date userinfoTime) {
        this.userinfoTime = userinfoTime;
    }

    public String getUserinfoCard() {
        return userinfoCard;
    }

    public void setUserinfoCard(String userinfoCard) {
        this.userinfoCard = userinfoCard;
    }

    public String getUserinfoMail() {
        return userinfoMail;
    }

    public void setUserinfoMail(String userinfoMail) {
        this.userinfoMail = userinfoMail;
    }

    public String getUserinfoPhone() {
        return userinfoPhone;
    }

    public void setUserinfoPhone(String userinfoPhone) {
        this.userinfoPhone = userinfoPhone;
    }

    public String getUserinfoSex() {
        return userinfoSex;
    }

    public void setUserinfoSex(String userinfoSex) {
        this.userinfoSex = userinfoSex;
    }
}